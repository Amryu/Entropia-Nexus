
<h1 style="text-align: center;">Verify your account</h1>

<p>
  A verified account allows you to contribute, manage your account and use all features of the site.<br />
  <br />
  To start verifcation join the <a href="https://discord.gg/hBGKyJ6EDr" target="_blank">Entropia Nexus Discord server</a> and follow the instructions below.
</p>
<ol>
  <li>Find your verification thread in the #verification channel</li>
  <li>Use the /seteuname command to set your Entropia Universe character name (e.g. /seteuname Pugnus Amryu Tonitrus)</li>
  <li>Wait for a moderator to generate a code for you</li>
  <li>Send the code to the moderators in-game character via PM or Mail - the character name will be in your verification thread</li>
  <li>Once your code is verified, your account will be upgraded to verified status</li>
</ol>