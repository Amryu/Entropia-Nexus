export function getTierMaterial(type, tier, index) {
  let mat = type === 'Weapon'
    ? weaponMats
    : type === 'ArmorSet'
    ? armorMats
    : type === 'MedicalTool'
    ? medicalToolMats
    : type === 'Finder'
    ? finderMats
    : type === 'Excavator'
    ? excavatorMats
    : null;

  if (!mat) return null;

  if (index === 0) {
    return genericMats.Components[tier - 1];
  }
  else if (index === 1) {
    return genericMats.Gems[tier - 1];
  }
  else if (index === 2) {
    return genericMats.Fragments[tier - 1];
  }
  else if (index === 3) {
    return mat.Material1[tier - 1];
  }
  else if (index === 4) {
    return mat.Material2[tier - 1];
  }
  else {
    return null;
  }
}