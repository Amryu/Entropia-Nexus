-- ============================================================================
-- PHASE 1: Add new enum values (outside transaction - PG limitation)
-- ============================================================================

DO $$ BEGIN
  ALTER TYPE "LocationType" ADD VALUE 'RevivalPoint';
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

DO $$ BEGIN
  ALTER TYPE "LocationType" ADD VALUE 'InstanceEntrance';
EXCEPTION WHEN duplicate_object THEN NULL;
