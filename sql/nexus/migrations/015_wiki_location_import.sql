-- ============================================================================
-- PHASE 2: Create LandAreas extension table
-- ============================================================================

CREATE TABLE IF NOT EXISTS "LandAreas" (
  "LocationId" INTEGER PRIMARY KEY REFERENCES "Locations"("Id") ON DELETE CASCADE,
  "TaxRate" NUMERIC,
  "OwnerId" INTEGER
  -- OwnerId references nexus_users "Users" table (cross-database, no FK constraint)
);

-- Audit table
CREATE TABLE IF NOT EXISTS "LandAreas_audit" (
  operation CHAR(1) NOT NULL,
  stamp TIMESTAMP NOT NULL,
  userid TEXT NOT NULL,
  "LocationId" INTEGER NOT NULL,
  "TaxRate" NUMERIC,
  "OwnerId" INTEGER
);

-- Set up inheritance (audit table inherits from main table)
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_inherits
    WHERE inhrelid = '"LandAreas_audit"'::regclass
      AND inhparent = '"LandAreas"'::regclass
  ) THEN
    ALTER TABLE "LandAreas_audit" INHERIT "LandAreas";
  END IF;
EXCEPTION WHEN undefined_table THEN NULL;
END $$;

-- Audit trigger function
CREATE OR REPLACE FUNCTION "LandAreas_audit_trigger"() RETURNS TRIGGER AS $func$
BEGIN
  IF (TG_OP = 'DELETE') THEN
    INSERT INTO "LandAreas_audit" SELECT 'D', now(), current_user, OLD.*;
    RETURN OLD;
  ELSIF (TG_OP = 'UPDATE') THEN
    INSERT INTO "LandAreas_audit" SELECT 'U', now(), current_user, NEW.*;
    RETURN NEW;
  ELSIF (TG_OP = 'INSERT') THEN
    INSERT INTO "LandAreas_audit" SELECT 'I', now(), current_user, NEW.*;
    RETURN NEW;
  END IF;
  RETURN NULL;
END;
$func$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS "LandAreas_audit_trigger" ON "LandAreas";
CREATE TRIGGER "LandAreas_audit_trigger"
  AFTER INSERT OR UPDATE OR DELETE ON "LandAreas"
  FOR EACH ROW EXECUTE FUNCTION "LandAreas_audit_trigger"();

-- ============================================================================
-- PHASE 3: Create MineralRarity enum and LandAreaMinerals cross table
-- ============================================================================

-- Create MineralRarity enum if it doesn't exist (also used by AreaMineables from migration 006)
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'MineralRarity') THEN
    CREATE TYPE "MineralRarity" AS ENUM (
      'Common',
      'Uncommon',
      'Rare',
      'Very Rare',
      'Extremely Rare'
    );
  END IF;
END$$;

CREATE TABLE IF NOT EXISTS "LandAreaMinerals" (
  "LocationId" INTEGER NOT NULL REFERENCES "Locations"("Id") ON DELETE CASCADE,
  "MaterialId" INTEGER NOT NULL,
  "Rarity" "MineralRarity" NOT NULL DEFAULT 'Common',
  "Notes" TEXT,
  PRIMARY KEY ("LocationId", "MaterialId")
);

CREATE INDEX IF NOT EXISTS "idx_landareaminerals_materialid" ON "LandAreaMinerals"("MaterialId");

-- Audit table
