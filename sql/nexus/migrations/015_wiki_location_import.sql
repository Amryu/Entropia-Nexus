INSERT INTO "Areas" ("LocationId", "Type", "Shape", "Data")
SELECT l."Id",
       w."AreaType"::"AreaType",
       'Circle'::"Shape",
       CASE WHEN w."Density" IS NOT NULL
         THEN jsonb_build_object('x', l."Longitude", 'y', l."Latitude", 'radius', 100, 'density', w."Density")
         ELSE jsonb_build_object('x', l."Longitude", 'y', l."Latitude", 'radius', 100)
       END
FROM "_wiki_import" w
JOIN ONLY "Locations" l ON l."Name" = w."Name"
  AND l."PlanetId" = w."PlanetId"
  AND l."Type" = 'Area'
  AND l."Longitude" = w."Longitude"
  AND l."Latitude" = w."Latitude"
WHERE w."LocationType" = 'Area'
  AND w."Name" != ''
  AND NOT EXISTS (
    SELECT 1 FROM ONLY "Areas" a WHERE a."LocationId" = l."Id"
  );

